package automation.execution.model;

public class TestRunBuild {

	private String testRunBuildId;
	private String testRunBuildName;
	private String releaseId;
	private String releaseName;
	private String createdBy;
	private String createdDate;
	private String applicationId;
	private String applicationName;
	
	public String getTestRunBuildId() {
		return testRunBuildId;
	}
	public void setTestRunBuildId(String testRunBuildId) {
		this.testRunBuildId = testRunBuildId;
	}
	public String getTestRunBuildName() {
		return testRunBuildName;
	}
	public void setTestRunBuildName(String testRunBuildName) {
		this.testRunBuildName = testRunBuildName;
	}
	public String getReleaseId() {
		return releaseId;
	}
	public void setReleaseId(String releaseId) {
		this.releaseId = releaseId;
	}
	public String getReleaseName() {
		return releaseName;
	}
	public void setReleaseName(String releaseName) {
		this.releaseName = releaseName;
	}
	public String getCreatedBy() {
		return createdBy;
	}
	public void setCreatedBy(String createdBy) {
		this.createdBy = createdBy;
	}
	public String getCreatedDate() {
		return createdDate;
	}
	public void setCreatedDate(String createdDate) {
		this.createdDate = createdDate;
	}
	public String getApplicationId() {
		return applicationId;
	}
	public void setApplicationId(String applicationId) {
		this.applicationId = applicationId;
	}
	public String getApplicationName() {
		return applicationName;
	}
	public void setApplicationName(String applicationName) {
		this.applicationName = applicationName;
	}
	
}
